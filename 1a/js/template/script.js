function somar(){
    let n1 = Number(prompt("Primeiro número: "))
    let n2 = Number(prompt("Segundo número: "))

    let soma = n1 + n2 

    alert("A soma deu: " + soma)
    
}

function subtrair(){
    let n1 = Number(prompt("Primeiro número: "))
    let n2 = Number(prompt("Segundo número: "))
    
    let diferenca = n1 - n2 
    
    alert("A diferença deu: " + diferenca)

}